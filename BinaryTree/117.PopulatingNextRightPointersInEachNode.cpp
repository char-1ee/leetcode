class Solution {
public:
    /** iterative */
    Node* connect(Node* root) {
        if (!root) return NULL;
        queue<Node*> q;
        q.push(root);
        while (!q.empty()) {
            int len = q.size();
            for (int i = 0; i < len; ++i) {
                Node* t = q.front(); q.pop();
                if (i < len - 1) t->next = q.front();
                if (t->left) q.push(t->left);
                if (t->right) q.push(t->right);
            }
        }
        return root;
    }

    /** recursive */
    Node* connectRe(Node* root) {
        if (!root) return nullptr;

        Node* p = root->next;
        while (p) {
            if (p->left) {
                p = p->left;
                break;
            }
            if (p->right) {
                p = p->right;
                break;
            }
            p = p->next;
        }
        if (root->left) root->left->next = root->right;
        if (root->right) root->right->next = (root->next == NULL) ? NULL : root->next->left;
        connect(p->left);
        connect(p->right);
        return root;
    }
};